
public class Artist {


	private String artistName;
	private String artistDate;
	private String artistGenre;
	private String artistOrigin;
	private String artistLabels;
	private String bioText;
	
	public String getArtistName() {
		return artistName;
	}
	public void setArtistName(String artistName) {
		this.artistName = artistName;
	}
	public String getArtistDate() {
		return artistDate;
	}
	public void setArtistDate(String artistDate) {
		this.artistDate = artistDate;
	}
	public String getArtistGenre() {
		return artistGenre;
	}
	public void setArtistGenre(String artistGenre) {
		this.artistGenre = artistGenre;
	}
	public String getArtistOrigin() {
		return artistOrigin;
	}
	public void setArtistOrigin(String aristOrigin) {
		this.artistOrigin = aristOrigin;
	}
	public String getArtistLabels() {
		return artistLabels;
	}
	public void setArtistLabels(String artistLabels) {
		this.artistLabels = artistLabels;
	}
	public String getBioText() {
		return bioText;
	}
	public void setBioText(String bioText) {
		this.bioText = bioText;
	}
}
